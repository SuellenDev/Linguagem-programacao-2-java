/*
Diferença entre atributo de instância e atributo estático:
Atributo de instância pertence a cada objeto criado. Já o atributo estático é compartilhado por todos os objetos da classe.

Por que utilizamos atributos private?
Para proteger os dados e controlar o acesso a eles por meio de métodos (getters e setters).

O que aconteceria se os atributos fossem public?
Qualquer parte do programa poderia alterar os valores diretamente, podendo gerar dados inválidos e erros.

Qual a função do construtor?
Inicializar os atributos do objeto no momento em que ele é criado.

Como o encapsulamento ajuda na manutenção do sistema?
Ele protege os dados, facilita alterações no código e reduz a chance de erros, tornando o sistema mais seguro e organizado.
*/
